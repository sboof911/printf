#include "libftprintf.h"

int		ft_atoi(const char *str)
{
	int		x;
	int		t;
	char	*s;

	s = (char *)str;
	x = 0;
	t = 1;
	while (*s == ' ' || *s <= '\n' || *s == '\v' || *s == '\r'
			|| *s == '\t' || *s == '\f')
	{
		s++;
	}
	if (*s == '-')
	{
		t *= -1;
		s++;
	}
	if (*s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		x = (x * 10) + *s - '0';
		s++;
	}
	return (x * t);
}
